    private void tambahMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tambahMouseClicked
        try {

            int ops =JOptionPane.showConfirmDialog(null, "ingin Menambahkan Data?", "tambah", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
            if(ops == JOptionPane.YES_OPTION){

                String simpan = "INSERT INTO data_nilai (nis, mapel, nilai) VALUES"
                + " ('"+nis.getText()+"','"+mapel.getSelectedItem()+"','"+nilai.getText()+"')";
                String simpan2 = "INSERT INTO siswa (nis, nm_sis, kls, id_jur) VALUES"
                + " ('"+nis.getText()+"','"+nama.getText()+"','"+kelas.getSelectedItem()+"','"+jurusan.getSelectedIndex()+"')";
                stat.addBatch(simpan);
                stat.addBatch(simpan2);
                stat.executeBatch();
                JOptionPane.showMessageDialog(null, "Berhasil Ditambahkan");
                TampilData();
                hapuslayar();
            }
            else{
                hapuslayar();
            }
        } catch (java.sql.SQLException e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_tambahMouseClicked

