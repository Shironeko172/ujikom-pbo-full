    private void editMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editMouseClicked
        try{
            int ops =JOptionPane.showConfirmDialog(null, "Apakah ingin diEdit?", "EDIT", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
            if(ops == JOptionPane.YES_OPTION){
                stat = con.createStatement();
                sql = "update data_nilai SET mapel = '"+mapel.getSelectedItem()+"', nilai = '"+nilai.getText()+ "'"
                + "WHERE id_nil='"+idnilai.getText()+"'";

                sql2 ="update siswa SET  nis= '"+nis.getText()+"', nm_sis = '"+nama.getText()+"', "
                + "kls = '"+kelas.getSelectedItem()+"', id_jur = '"+jurusan.getSelectedIndex()+ "'"
                + "WHERE ids='"+ids.getText()+"'";

                stat.addBatch(sql);
                stat.addBatch(sql2);
                stat.executeBatch();

                JOptionPane.showMessageDialog(null, "Berhasil Diedit");
                TampilData();
                hapuslayar();
            }else{
                hapuslayar();
            }
        }catch(Exception exc){
            System.err.println(exc.getMessage());
        }
    }//GEN-LAST:event_editMouseClicked

